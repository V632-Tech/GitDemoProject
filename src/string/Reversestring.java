package string;

public class Reversestring {
	public static void main(String[] args) {
		
		String s26 ="abcdef";
	     String reverse ="";
	     for(int i = (s26.length()-1); i>=0 ;i--) 
	     {
	    	reverse = reverse+s26.charAt(i) ;
	    	
	     }
	     System.out.println("reverse string is :"+reverse);
	    		 			
		
	}
     			

}
